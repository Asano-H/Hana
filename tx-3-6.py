#等価演算子と関係演算子
print("２つの整数を入力してください")
print("整数Ａ：",end="")
a = int(input())
print("整数Ｂ：",end="")
b = int(input())

print("等価式の値")
print("Ａ == Ｂ の値：",a == b)
print("Ａ != Ｂ の値：",a != b)

print("関係式の値")
print("Ａ <  Ｂ の値：",a<b)
print("Ａ <= Ｂ の値：",a<=b)
print("Ａ >  Ｂ の値：",a<b)
print("Ａ >= Ｂ の値：",a>=b)